public class Division {
    double a;
    double b;

    // Constructor para inicializar los valores de 'a' y 'b'
    public Division(double a, double b) {
        this.a = a;
        this.b = b;
    }

    // Metodo para realizar la suma
    public double calcularDivision() {
        return a / b;
    }
}
